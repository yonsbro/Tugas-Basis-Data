public class ContohVariabel {
    public static void main(String[] args) {
        // Deklarasi variabel
        int jumlahBaris = 5;
        
        // Menggunakan variabel
        System.out.println("Jumlah baris: " + jumlahBaris);
        
        // Mengubah nilai variabel
        jumlahBaris = 8;
        System.out.println("Jumlah baris terbaru: " + jumlahBaris);
    }
}